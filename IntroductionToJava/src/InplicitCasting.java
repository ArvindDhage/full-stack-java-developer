public class InplicitCasting {
    public static void main(String[] args){
        // Inplicit Casting :- cast a data type from lower to upper.
        // ** No data lose.
        int a=100;
        int b=a; //int ->long
        System.out.println(b);
        Short s=12;
        int c=s;
        System.out.println(c);
    }
}
