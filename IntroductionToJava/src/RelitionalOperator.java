/*
Relational Operator
-> Used to test Comparision between Operands or Values.
(==,>,>=,<,<=,!=)

*/

public class RelitionalOperator {
    public static void main(String args[]){
     int a =10;
     int b=20;
        System.out.println(a==b);
        System.out.println(a>b);
        System.out.println(a>=b);
        System.out.println(a<b);
        System.out.println(a<=b);
        System.out.println(a!=b);
    }
}
